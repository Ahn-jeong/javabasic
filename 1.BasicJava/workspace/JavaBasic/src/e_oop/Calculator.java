package e_oop;

public class Calculator {

	double sum(double i, double j){
		return i + j;
	}
	double minus(double i, double j){
		return i - j;
	}
	double multiple(double i, double j){
		return i * j;
	}
	double divide(double i, double j){
		return i / j;
	}
	double low(double i, double j){
		return i % j;
	}
}
